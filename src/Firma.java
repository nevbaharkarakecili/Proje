
public class Firma {
    private String firmaAd,testYeri;

    public Firma(String firmaAd, String testYeri) {
        this.firmaAd = firmaAd;
        this.testYeri = testYeri;
    }

    public String getFirmaAd() {
        return firmaAd;
    }

    public void setFirmaAd(String firmaAd) {
        this.firmaAd = firmaAd;
    }

    public String getTestYeri() {
        return testYeri;
    }

    public void setTestYeri(String testYeri) {
        this.testYeri = testYeri;
    }
    
}
